package basic;  // package name 

public class DataTypes {   // class body

	// main method - starting point 
	public static void main(String[] args) {  // Method body
		
//		1. int - DataType
		
		// int - DataType, a - variable, 10 - value
		// Duplicate variables are not allowed in Java
		int a = 10; 
		int b = 20;
		int c = 105;
		
		System.out.println(a);
		System.out.println(c+b);
		
//		2. double - DataType
		double d = 33.33;
		double d1 = 100;
		System.out.println(d1);
		
//		3. char - dataype
		char c1 = 'A';
		char c2 = '@';
		char c3 = 'C';
		
		System.out.println(c3);
		
//		4. Boolean - dataype
		boolean b1 = true;
		boolean b2 = false;
		
//		5. String - Datatype
		
		String str = "Dev";
		String str2 = "12345";
		String str3 = "!@#$%^&*";
		
		System.out.println(str2);

	}

}
