package basic;

public class FoorLooping {

	//	 i++ => i = i+1
	public static void main(String[] args) {

		// Initialization, condition & Increment
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		System.out.println("**************8");

		// Initialization, condition & Decrement
		for (int j = 10; j >= 1; j--) {
			System.out.println(j);
		}

	}

}
